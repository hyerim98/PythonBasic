# 변수
name = "홍길동"
animal = "호랑이"
age = 4
hobby = "산책"
is_adult = age >= 3

print(animal + "의 이름은 " + name)
print(name + "은 " + str(age) + "살이며," + hobby + "을 좋아함")
# '+'로 문장을 이어가면 Type을 Str로 맞춰줘야 한다
print(name + "은 어른일까요?  : " + str(is_adult))
# ','로 문장을 이어가면 Type이 달라도 상관없다
print(name , "은 어른일까요?  : " , is_adult)



